package OperatorBasic;

public class operator6logical {
	public static void main(String[] args) {  
		
		System.out.println((2>1)&&(12>3));
		System.out.println((2>1) && (12<3));
		System.out.println((2>1) || (2>1));
		System.out.println((2>1)||(12<3));
		System.out.println(!(3==5));
		System.out.println(!(5>3));
		
	}

	
	}

	


