package OperatorBasic;

public class operator1 {
	public static void main(String[] args) {
		
		
		int a=10,b=6;
		
		System.out.println("a+b="+(a+b));
		
		
		System.out.println("a-b=" +(a - b));         //() is mandatory after + ex.+(a-b)
		
		
		System.out.println("a*b="+(a*b));
		
		
		System.out.println("a/b="+(a/b));
		
		
		System.out.println("a%b="+(a%b));            //Module operator
		
		
	}

}
