package OperatorBasic;

public class operator2 {   
	
	public static void main (String [] args)
	{
		float a=12.5f,b=2.2f;
		
	System.out.println("a+b="+(a+b));
		
		
		System.out.println("a-b=" +(a - b));//() is mandatory after +  ex.+(a-b)
		
		
		System.out.println("a*b="+(a*b));
		
		
		System.out.println("a/b="+(a/b));
		
		
		System.out.println("a%b="+(a%b));
		
		
		
		
	}

}
